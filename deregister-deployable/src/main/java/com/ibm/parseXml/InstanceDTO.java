package com.ibm.parseXml;

public class InstanceDTO {

	private String eurekaInstanceID;
	private String eurelaApplicationName;

	/**
	 * @return the eurekaInstanceID
	 */
	public String getEurekaInstanceID() {
		return eurekaInstanceID;
	}

	/**
	 * @param eurekaInstanceID
	 *            the eurekaInstanceID to set
	 */
	public void setEurekaInstanceID(String eurekaInstanceID) {
		this.eurekaInstanceID = eurekaInstanceID;
	}

	/**
	 * @return the eurelaApplicationName
	 */
	public String getEurelaApplicationName() {
		return eurelaApplicationName;
	}

	/**
	 * @param eurelaApplicationName
	 *            the eurelaApplicationName to set
	 */
	public void setEurelaApplicationName(String eurelaApplicationName) {
		this.eurelaApplicationName = eurelaApplicationName;
	}

}
